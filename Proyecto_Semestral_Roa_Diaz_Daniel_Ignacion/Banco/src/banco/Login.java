  if (ClaseCuenta.Validar(TxtUsuario.getText(), TxtPassword.getText())) {        
        this.dispose();   
        
        if (TxtUsuario.getText().equals("David"))   {
            FrmAltaCuentas FrmCtas = new FrmAltaCuentas();
            FrmCtas.setVisible(true);   
        }
        else{
                FrmMovimientos FrmMov = new FrmMovimientos(TxtUsuario.getText());
                FrmMov.setVisible(true);   
            }
    }else{
        JOptionPane.showMessageDialog(this,"Nombre de Usuario y/o Contrase√±a incorrecto");
    }
